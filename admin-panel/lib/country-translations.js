// Fonction pour traduire un nom de pays
export function translateCountry(countryName, language = 'fr') {
  if (!countryName) return '';
  
  const translations = countryTranslations[language] || countryTranslations.fr;
  return translations[countryName] || countryName;
}

// Fonction pour traduire une liste de pays
export function translateCountries(countries, language = 'fr') {
  if (!Array.isArray(countries)) return [];
  
  return countries.map(country => translateCountry(country, language));
}

